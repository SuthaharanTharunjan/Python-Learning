while True:
    num_1=float(input("Enter number 1: "))
    num_2=float(input("Enter number 2: "))
    function=str(input("Enter the function:"))
    if function=="*":
        print(num_1*num_2)
    elif function=="/":
        print(num_1/num_2)
    elif function=="+":
        print(num_1+num_2)
    elif function=="-":
        print(num_1-num_2)


